int		ft_is_prime(int nb)
{
	int i;
	int prime;

	i = 1;
	prime = 0;
	if (nb <= 1)
		return (0);
	while (i <= nb)
	{
		if (nb % i == 0)
			prime++;
		if (prime > 2)
			return (0);
		i++;
	}
	return (1);
}
