int		ft_sqrt(int nb)
{
	int i;

	i = nb;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (nb > 0)
	{
		if (i == nb * nb)
			return (nb);
		nb--;
	}
	return (0);
}
